// 순수 로직 검증 (jsc) — gas/Code.gs stayStage_ 와 동일 구현.
// 배경: 2026-07-24 Rosie 건 — 도착 완료 게스트에게 "체크인 정보 곧 발송" 오답. 프롬프트에 예약 단계 부재가 원인.
function stayStage_(today, ci, co) {
  if (!ci && !co) return null;
  if (co && today > co) return '체크아웃 후(이미 퇴실 — 도착·체크인 안내 금지)';
  if (co && today === co) return '체크아웃일(11:00까지 퇴실)';
  if (ci && today < ci) return '도착 전';
  if (ci && today === ci) return '체크인 당일(15:00부터 입실)';
  return '숙박 중(이미 입실)';
}

var pass = 0, fail = 0;
function eq(n, g, w) { var okk = g === w; if (okk) pass++; else fail++; print((okk ? '  PASS ' : '  FAIL ') + n + (okk ? '' : '  got=' + g + ' want=' + w)); }

print('[1] 기본 단계');
eq('도착 전',        stayStage_('2026-07-20', '2026-07-24', '2026-07-25'), '도착 전');
eq('체크인 당일',    stayStage_('2026-07-24', '2026-07-24', '2026-07-25'), '체크인 당일(15:00부터 입실)');
eq('체크아웃일',     stayStage_('2026-07-25', '2026-07-24', '2026-07-25'), '체크아웃일(11:00까지 퇴실)');
eq('체크아웃 후',    stayStage_('2026-07-26', '2026-07-24', '2026-07-25'), '체크아웃 후(이미 퇴실 — 도착·체크인 안내 금지)');
eq('숙박 중(장기)',  stayStage_('2026-07-22', '2026-07-20', '2026-07-25'), '숙박 중(이미 입실)');

print('[2] 결측 처리');
eq('날짜 전무 → null(블록에 단계 미표기)', stayStage_('2026-07-24', null, null), null);
eq('체크인만 있음·당일',   stayStage_('2026-07-24', '2026-07-24', null), '체크인 당일(15:00부터 입실)');
eq('체크아웃만 있음·경과', stayStage_('2026-07-26', null, '2026-07-25'), '체크아웃 후(이미 퇴실 — 도착·체크인 안내 금지)');

print('[3] Rosie 재현 — 7/24 23:17 도착 메시지, 7/24~7/25 숙박 → 체크인 당일(입실 완료 가능) 판정');
eq('Rosie', stayStage_('2026-07-24', '2026-07-24', '2026-07-25'), '체크인 당일(15:00부터 입실)');

print('결과: ' + pass + ' PASS / ' + fail + ' FAIL');
